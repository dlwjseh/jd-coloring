/// 색칠 팔레트 — 프리즈마컬러 Premier 72색 (8행 × 9열). 디자인 스펙 §16-2.
/// 펼침 그리드는 `rows` 순서대로, 독/기본 선택은 `all` 사용.
enum Palette {
    static let rows: [[Color]] = [
        [0xF6E7B5, 0xB98A5A, 0xF8EE4A, 0xF5D93A, 0xE6A13B, 0xC89A3C, 0xD97A1F, 0xC89126, 0xF5B223],
